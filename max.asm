SSTACK SEGMENT PARA STACK 'STACK'
	DB 64 DUP (?)
SSTACK ENDS

DSEG SEGMENT PARA PUBLIC 'DATA'
	X DB 3, 2, 9, 4, 5, 6, 7
DSEG ENDS

CSEG SEGMENT PUBLIC 'CODE'
	ASSUME CS:CSEG, DS:DSEG, SS: SSTACK

START PROC FAR
	MOV AX, DSEG
	MOV DS, AX
M1:	MOV SI, 0
	MOV DI, 0
	MOV CX, 7

M2:	INC SI
	mov bh, X[DI]
	mov bl, X[SI]
	CMP bl, bh
	JG M4
	JMP M3

M4: 	MOV DI, SI
	JMP M3

M3:	TEST SI, CX
	JZ PRINT
	LOOP M2

PRINT:	MOV SI, DI
	ADD X[SI], '0'
	MOV AH, 2h
	MOV DX, [SI]
	INT 21h
EXIT:	MOV AH, 4ch
	MOV AL, 0
	INT 21h
START ENDP
CSEG ENDS
END START
	
